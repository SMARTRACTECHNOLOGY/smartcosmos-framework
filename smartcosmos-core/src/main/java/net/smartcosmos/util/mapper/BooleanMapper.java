public class BooleanMapper implements IMetadataValueMapper<Boolean>
{
    @Override
    public String toString(Boolean value)
    {
        if (value)
        {
            return "true";
        } else
        {
            return "false";
        }
    }

    @Override
    public Boolean fromString(String rawValue)
    {
        if (rawValue.equalsIgnoreCase("true"))
        {
            return true;
        }
        return false;
    }
}
