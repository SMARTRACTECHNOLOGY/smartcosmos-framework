import com.fasterxml.jackson.annotation.JsonView;
import net.smartcosmos.model.base.INamedObject;
import net.smartcosmos.util.json.JsonGenerationView;

import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

public abstract class NamedObject<T> extends DomainResource<T>implements INamedObject<T>
{
    @JsonView(JsonGenerationView.Published.class)
    @Size(max = NAME_LENGTH)
    @NotNull
    protected String name;

    @JsonView(JsonGenerationView.Standard.class)
    @Size(max = DESCRIPTION_LENGTH)
    protected String description;

    @JsonView(JsonGenerationView.Standard.class)
    protected boolean activeFlag;

    @Override
    public String getName()
    {
        return name;
    }

    @Override
    public void setName(String name)
    {
        this.name = name;
    }

    @Override
    public String getDescription()
    {
        return description;
    }

    @Override
    public void setDescription(String description)
    {
        this.description = description;
    }

    @Override
    public boolean isActive()
    {
        return activeFlag;
    }

    @Override
    public void setActive(boolean flag)
    {
        this.activeFlag = flag;
    }

    @Override
    public boolean equals(Object obj)
    {
        if (this == obj)
            return true;
        if (!super.equals(obj))
            return false;
        if (getClass() != obj.getClass())
            return false;
        NamedObject other = (NamedObject) obj;
        if (activeFlag != other.activeFlag)
            return false;
        if (description == null)
        {
            if (other.description != null)
                return false;
        } else if (!description.equals(other.description))
            return false;
        if (name == null)
        {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        return true;
    }

    @Override
    public int hashCode()
    {
        final int prime = 31;
        int result = super.hashCode();
        result = prime * result + (activeFlag ? 1231 : 1237);
        result = prime * result + ((description == null) ? 0 : description.hashCode());
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        return result;
    }
}
