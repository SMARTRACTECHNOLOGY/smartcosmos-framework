import net.smartcosmos.model.context.RoleType;

import javax.ws.rs.core.Response;
import java.lang.annotation.Documented;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import static java.lang.annotation.ElementType.METHOD;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

@Retention(RUNTIME)
@Target(METHOD)
@Documented
public @interface PermissionsCheck
{
    String key();

    boolean checkRole() default false;

    RoleType roleType() default RoleType.User;

    Response.Status noPermissionStatus() default Response.Status.UNAUTHORIZED;

    int code() default 0;

    String message() default "PermissionsCheck Failure";
}




